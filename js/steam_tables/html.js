export function escapeHtml(value) {
    return String(value ?? '')
        .replace(/&/g, '&amp;')
        .replace(/</g, '&lt;')
        .replace(/>/g, '&gt;')
        .replace(/"/g, '&quot;')
        .replace(/'/g, '&#39;');
}

export function phaseClass(phase) {
    return String(phase).replace(/[^a-z0-9]+/gi, '-').toLowerCase();
}
